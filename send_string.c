#include "client.h"

/* ************************************************************************** */
static int ft_strlen(char *str);

/* ************************************************************************** */
void	send_string(char *str, int pid, t_data *data)
{
    int len;
    int i;

    len = ft_strlen(str);
    i = 0;
    while (i < len)
    {
        send_char((int)str[i], pid, data);
        i++;
    }
    // send_char(str[i], pid, data);
}
/* ************************************************************************** */
static int ft_strlen(char *str)
{
    int i;
    
    if (!str)
        return (0);

    i = 0;
    while(str[i] != '\0')
        i++;

    return (i);
}
